class Coordinate {
    private int x, y;
    public Coordinate(int i, int j) {
        x = j;
        y = i;
    }
    public Integer getX() {
        return x;
    }
    public Integer getY() {
        return y;
    }

    public void setX(int n) {
        this.x =n;
    }
    public void setY(int k) {
        this.y = k;
    }
}